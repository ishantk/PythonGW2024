# String in Action
# Textual Data Type

name = 'John'
cafe_name = "John's Cafe"

# Multi Line String
message = """Hi
How Are you..
This is John from John's Cafe
"""

# Raw String -> Accepts special characters as data
# Regular Expression uses Raw String :)
cafe_name = r'John\'s \nCafe'

print(name)
print(cafe_name)
print(message)
